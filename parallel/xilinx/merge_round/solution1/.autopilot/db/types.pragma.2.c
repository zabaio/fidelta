void swap_pts(point *a,point *b){
    point temp=*a;
    *a=*b;
    *b=temp;
}


void order_two_pts(point *a,point *b){
    if(a->id > b->id)
        swap_pts(a,b);
    return;
}



void order_three_pts(point *a,point *b,point *c){
    if(!(a->x < b->x || (a->x == b->x && a->y <= b->y))) swap_pts(a,b);
    if(!(a->x < c->x || (a->x == c->x && a->y <= c->y))) swap_pts(a,c);

    float xca = a->x - c->x;
    float xcb = b->x - c->x;
    float yca = a->y - c->y;
    float ycb = b->y - c->y;
    float det = xca*ycb - xcb*yca;

    if (det<0){
        swap_pts(b,c);
    }

    return;
}


